#include <iostream>
using namespace std;

int main()
{
	int x, y;

	cout << "두개의 정수를 입력하세요: ";
	cin >> x >> y;

	cout << x << " + " << y << " = " << x + y << endl;
	cout << x << " - " << y << " = " << x - y << endl;
	cout << x << " * " << y << " = " << x * y << endl;
	cout << x << " / " << y << " = " << x / y << endl;
	cout << x << " % " << y << " = " << x % y << endl;
}
